    public void tabla() {
        String[] columnas = { "CÃ³digo", "Titulo", "Autor","Categoria", "ISBN", "AÃ±o", "Precio"};
        Object[] obj = new Object[7];
        DefaultTableModel tabla = new DefaultTableModel(null, columnas);
        List ls;
        try {
            ls = daoLibro.mostrarLibro();
            for (int i = 0; i < ls.size(); i++) {
                libro = (Libro) ls.get(i);
                obj[0] = libro.getLibroID();
                obj[1] = libro.getTitulo();
                obj[2] = libro.getNombreAutor();
                obj[3] = libro.getNombreCategoria();
                obj[4] = libro.getISBN();
                obj[5] = libro.getAnnioPublicacion();
                obj[6] = libro.getPrecio();
                tabla.addRow(obj);
            }
            ls = daoLibro.mostrarLibro();
            this.tbl_libros.setModel(tabla);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al mostrar datos del categoria" + 
            e.toString());
        }
    }
    
    public void llenarTabla() {
        int fila = this.tbl_libros.getSelectedRow();
        this.txt_codigo.setText(String.valueOf(this.tbl_libros.getValueAt(fila, 0)));
        this.txt_nombre.setText(String.valueOf(this.tbl_libros.getValueAt(fila, 1)));
        String autor = String.valueOf(this.tbl_libros.getValueAt(fila, 2));
        this.cmb_autor.setSelectedItem(autor);

        String categoria = String.valueOf(this.tbl_libros.getValueAt(fila, 3));
        this.cmb_categoria.setSelectedItem(categoria);this.txt_isbn.setText(String.valueOf(this.tbl_libros.getValueAt(fila, 4)));
        this.txt_annio.setText(String.valueOf(this.tbl_libros.getValueAt(fila, 5)));
        this.txt_precio.setText(String.valueOf(this.tbl_libros.getValueAt(fila, 5)));
    }
    public void agregar() throws Exception {
        libro.setTitulo(this.txt_nombre.getText());
        String autorSeleccionada = (String) cmb_autor.getSelectedItem();
        int idAutor = daoAutor.obtenerIdAutor(autorSeleccionada);
        libro.setAutorID(idAutor);
        
        String categoriaSeleccionada = (String) cmb_categoria.getSelectedItem();
        int idCategoria = daoCat.obtenerIdCategoria(categoriaSeleccionada);
        libro.setCategoriaID(idCategoria);
        
        libro.setISBN(this.txt_isbn.getText());
        libro.setAnnioPublicacion( Integer.parseInt(this.txt_annio.getText()));
        libro.setPrecio( Double.parseDouble(this.txt_precio.getText()) );
        daoLibro.crearLibro(libro);
        JOptionPane.showMessageDialog(null, "Datos insertados con Ã©xito");
        
        limpiar();
    }
    
    public void actualizar() {
        try {
            libro.setLibroID(Integer.parseInt(this.txt_codigo.getText()));
            libro.setTitulo(this.txt_nombre.getText());
            libro.setAutorID(this.cmb_autor.getSelectedIndex());
            libro.setCategoriaID(this.cmb_categoria.getSelectedIndex());
            libro.setISBN(this.txt_isbn.getText());
            libro.setAnnioPublicacion( Integer.parseInt(this.txt_annio.getText()));
            libro.setPrecio( Double.parseDouble(this.txt_precio.getText()) );
            int decision = JOptionPane.showConfirmDialog(this, "Desea modificar al categoria", 
            "Modificar categoria", JOptionPane.YES_NO_OPTION);
            if (decision == 0) {
                daoLibro.actualizarLibro(libro);
                JOptionPane.showMessageDialog(rootPane, "Proveedor modificado con Ã©xito", 
                "ConfirmaciÃ³n", JOptionPane.INFORMATION_MESSAGE);
                tabla();
                limpiar();
            } else {
                limpiar();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    public void eliminar() {
        try {
            libro.setLibroID(Integer.parseInt(this.txt_codigo.getText()));
            int decision = JOptionPane.showConfirmDialog(this, "Desea eliminar categoria", 
            "Eliminar categoria", JOptionPane.YES_NO_OPTION);
            if (decision == 0) {
                daoLibro.eliminarLibro(libro);
                JOptionPane.showMessageDialog(rootPane, "Eliminado con Ã©xito", "ConfirmaciÃ³n",
                JOptionPane.INFORMATION_MESSAGE);
                tabla();
                limpiar();
            } else {
                limpiar();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.toString(), "Error", 
            JOptionPane.ERROR_MESSAGE);
        }
    }
    public void limpiar() {
        this.txt_codigo.setText("");
        this.txt_nombre.setText("");
    }


