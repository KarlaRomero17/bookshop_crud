    public void llenarCmbCat(){
        try { 
            List<Categoria> departamentos = daoCat.mostrarCategoria();
            String[] nombresDepartamentos;
            nombresDepartamentos = new String[departamentos.size()];
            cmb_categoria.addItem("Seleccionar");
            for (int i = 0; i < departamentos.size(); i++) {
                Categoria dep = departamentos.get(i);
                nombresDepartamentos[i] = dep.getNombreCategoria();
                cmb_categoria.addItem(nombresDepartamentos[i]);
            }
            Modelo = new DefaultComboBoxModel<>(nombresDepartamentos);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
     
